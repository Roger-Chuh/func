function res =  RightMultiSkew(R, n) 
  res = eye(3);
  res(0 + 1, 0 + 1) = R(0 + 1, 1 + 1) * n(2 + 1) - R(0 + 1, 2 + 1) * n(1 + 1);
  res(0 + 1, 1 + 1) = R(0 + 1, 2 + 1) * n(0 + 1) - R(0 + 1, 0 + 1) * n(2 + 1);
  res(0 + 1, 2 + 1) = R(0 + 1, 0 + 1) * n(1 + 1) - R(0 + 1, 1 + 1) * n(0 + 1);
  res(1 + 1, 0 + 1) = R(1 + 1, 1 + 1) * n(2 + 1) - R(1 + 1, 2 + 1) * n(1 + 1);
  res(1 + 1, 1 + 1) = R(1 + 1, 2 + 1) * n(0 + 1) - R(1 + 1, 0 + 1) * n(2 + 1);
  res(1 + 1, 2 + 1) = R(1 + 1, 0 + 1) * n(1 + 1) - R(1 + 1, 1 + 1) * n(0 + 1);
  res(2 + 1, 0 + 1) = R(2 + 1, 1 + 1) * n(2 + 1) - R(2 + 1, 2 + 1) * n(1 + 1);
  res(2 + 1, 1 + 1) = R(2 + 1, 2 + 1) * n(0 + 1) - R(2 + 1, 0 + 1) * n(2 + 1);
  res(2 + 1, 2 + 1) = R(2 + 1, 0 + 1) * n(1 + 1) - R(2 + 1, 1 + 1) * n(0 + 1);
end


